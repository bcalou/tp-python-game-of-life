class Game:
    """Main logic class
    """
    def __init__(self, width, height, board):
        """Init the board with constants above
        """
        self.width = width
        self.height = height
        self.cells = [[board[y][x] for x in range(width)] for y in range(height)]

    def get_neighbors(self, x, y):
        """Get neighbors cells from coordinates
        """
        neighbors = []
        for i in [-1, 0, 1]:
            for j in [-1, 0, 1]:
                if i == 0 and j == 0:  # out of bounds
                    continue

                if y + i < 0 or y + i >= self.height:  # out of bounds
                    continue
                if x + j < 0 or x + j >= self.width:  # out of bounds
                    continue

                neighbors.append(
                    self.cells[(y + i) % self.height][(x + j) % self.width]
                )
        return neighbors

    def get_next_state(self, x, y):
        """Calculate next state for each cell in the board
        """
        neighbors = self.get_neighbors(x, y)  # Get the neighbors of the cell
        neighbors_alive = 0  # Count alive neighbors
        for neighbor in neighbors:
            if neighbor:
                neighbors_alive += 1

        if neighbors_alive < 2 or neighbors_alive > 3:
            return 0  # Cell dies
        elif neighbors_alive == 3:
            return 1  # Cell becomes alive
        else:
            return self.cells[y][x]  # No change

    def update(self):
        """Board update logic
        """
        # Load a temporary board state (but don't update the actual board yet)
        next_state = [[0 for _x in range(self.width)] for _y in range(self.height)]
        for row in range(self.height):
            for column in range(self.width):
                next_state[row][column] = self.get_next_state(column, row)

        # Update the board
        for row in range(self.height):
            for column in range(self.width):
                self.cells[row][column] = next_state[row][column]


# Main loop
