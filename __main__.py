    # Draw background
    screen.fill(dead)

    # Draw cells
    for row in range(board_height):
        for column in range(board_width):
            if game.cells[row][column] == 1:
                pygame.draw.rect(
                    screen,
                    live,
                    [
                        column * cell_width,
                        row * cell_height,
                        cell_width,
                        cell_height,
                    ],
                )

    # Update board
    game.update()

    # Update display
    pygame.display.flip()
    clock.tick(int(os.getenv("FPS") or 60))
