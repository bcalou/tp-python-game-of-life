# We suppose that all row and all column have the same number of elements
MATRICE_NUMBER_IN_HEIGHT: int = len(MATRICE)
MATRICE_NUMBER_IN_WIDTH: int = len(MATRICE[0])


def minimum(cell_by_height: int, cell_by_width: int):
    """Return the minimum"""
    return cell_by_height if cell_by_height < cell_by_width else cell_by_width


# We define the size of all cell with the size of the matrix
MINIMUM_CELL_LENGTH = minimum(SCREEN_HEIGHT // MATRICE_NUMBER_IN_HEIGHT,
                              SCREEN_WIDTH // MATRICE_NUMBER_IN_WIDTH)


CELL_ALIVE: int = 1
CELL_DEAD: int = 0
MIN_NEIGBOUR_AROUND_FOR_LIVE: int = 2
MAX_NEIGBOUR_AROUND_FOR_LIVE: int = 3
MIN_NEIGBOUR_AROUND_FOR_BORN: int = 3
MAX_NEIGBOUR_AROUND_FOR_BORN: int = 3


class Matrix_State:

    __state: list[list[int]] = []
    __tab_index: int
    __element_index: int
    __tab_length: int

    def get_state(self) -> list[list[int]]:
        return self.__state

    def state_choose(self) -> None:
        """We choose state of matrix"""

        # if matrix isn't empty we change matrix step else we initiate it
        self.__get_next_state() if len(self.__state) != 0 \
            else self.__get_first_state()

    def __get_first_state(self) -> None:
        """We define the matrix that we will use according to size of screen"""

        # if matrix fill all screen in height and width we don't change matrix
        if SCREEN_HEIGHT // MATRICE_NUMBER_IN_HEIGHT\
           == SCREEN_WIDTH // MATRICE_NUMBER_IN_WIDTH:
            self.__state = MATRICE

        # else we complete matrix in height or width
        else:
            self.__complete_screen_length()

    def __complete_screen_length(self) -> None:
        """We complete the matrix width or height
        to adapt it to the size of window"""

        # We define which length we want complete and length of screen max
        screen_and_length: list[int] = self.__length_and_screen_to_complete()
        matrix_length_to_complete: int = screen_and_length[0]
        increment: int = screen_and_length[0]
        screen_to_complete = screen_and_length[1]

        # We duplicate matrix to modify it
        state: list[list[int]] = MATRICE

        # if we complete height we add line full of 0 without change the size
        # of existing lines. if we complete width, we add zeros at the end of
        # existing lines without create new line
        while (increment * MINIMUM_CELL_LENGTH < screen_to_complete -
               MINIMUM_CELL_LENGTH):
            if (matrix_length_to_complete == MATRICE_NUMBER_IN_HEIGHT and
               screen_to_complete == SCREEN_HEIGHT):
                state.append([])
            for second_increment in range(MATRICE_NUMBER_IN_HEIGHT):
                if matrix_length_to_complete == MATRICE_NUMBER_IN_HEIGHT\
                   and screen_to_complete == SCREEN_HEIGHT:
                    state[increment].append(0)
                else:
                    state[second_increment].append(0)
            increment += 1
        self.__state = state

    def __length_and_screen_to_complete(self) -> list[int]:
        """We find which part of matrix we need to complete"""

        # if cell size multiply by number of cells, fill all the height or
        # all the width, we complete the other part
        if SCREEN_HEIGHT // MATRICE_NUMBER_IN_HEIGHT\
           != MINIMUM_CELL_LENGTH:
            return [MATRICE_NUMBER_IN_HEIGHT, SCREEN_HEIGHT]
        return [MATRICE_NUMBER_IN_WIDTH, SCREEN_WIDTH]

    def __get_next_state(self) -> None:
        """We pass matrix to next step"""

        # We create a new state, then we add every cell of the state
        # to the new state after change their value.
        new_state: list[list[int]] = []
        for self.__tab_index, tab in enumerate(self.__state):
            new_state.append([])
            self.__tab_length = len(tab)
            for self.__element_index, element in enumerate(tab):
                self.__append_cell(new_state, element)
        self.__state = new_state

    def __append_cell(self, new_state: list[list[int]], element: int) -> None:
        """We decide if cell will be alive next turn or not"""

        # After verify all alive neighbour
        life_cell_count: int = self.__neighbour_cell_count()

        # We check if the cell can born or can rest alive
        if (MIN_NEIGBOUR_AROUND_FOR_LIVE <= life_cell_count
           <= MAX_NEIGBOUR_AROUND_FOR_LIVE and element == CELL_ALIVE) or \
           (MIN_NEIGBOUR_AROUND_FOR_BORN <= life_cell_count <=
           MAX_NEIGBOUR_AROUND_FOR_BORN and element == CELL_DEAD):
            new_state[self.__tab_index].append(CELL_ALIVE)
        else:
            new_state[self.__tab_index].append(CELL_DEAD)

    def __neighbour_cell_count(self) -> int:
        """ We check neighbour for a cell in the matrix"""

        # We find all neighbour alive around cell and we return this number
        # neighbour are cell who are -/+1 in position x or y in the tab
        life_cell_count: int = 0
        for neighbour_y in range(-1, 2):
            for neighbour_x in range(-1, 2):
                life_cell_count += self.__neighbour_verify(neighbour_x,
                                                           neighbour_y)
        return life_cell_count

    def __neighbour_verify(self, neighbour_x: int, neighbour_y: int) -> int:
        """it is verify neighbour of a case """

        # it is verify if neighbour of case are in matrix and arn't case itself
        # then it is check if it is alive or not
        if ((neighbour_x != 0 or neighbour_y != 0) and
           (0 <= self.__element_index + neighbour_x <= self.__tab_length - 1)
           and (0 <= self.__tab_index + neighbour_y <= len(self.__state) - 1)):
            if (self.__state[self.__tab_index+neighbour_y]
               [self.__element_index+neighbour_x] == 1):
                return CELL_ALIVE
        return CELL_DEAD
